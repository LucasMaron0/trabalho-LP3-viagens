    public void setComboBoxAvioes(){
         jComboBoxAvioes.removeAllItems();
        try{
           List<Aviao> avioes = aviaoImpl.getAllAvioes();
           for(Aviao a: avioes){
               if(!a.isEmViagem()){
                    jComboBoxAvioes.addItem(a);
               }             
           }
           
        }catch(Exception ex){
            System.out.print(ex.getMessage());
        }
    }

