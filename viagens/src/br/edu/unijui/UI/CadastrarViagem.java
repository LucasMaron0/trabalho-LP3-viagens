    private void jButtonCadastrarViagemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonCadastrarViagemMouseClicked
        try{
            Aviao aviao = (Aviao) jComboBoxAvioes.getSelectedItem();
            Viagem viagem = new Viagem(
                    aviao, 
                    jTextFieldOrigem.getText(), 
                    jTextFieldDestino.getText(),
                    jTextFieldData.getText());
           
            viagemImpl.inserirViagem(viagem);
            AdministrarViagens adminViagensTela = new AdministrarViagens();
            adminViagensTela.setVisible(true);
            this.dispose();
        }catch(Exception ex){
             Logger.getLogger(CadastrarViagem.class.getName()).log(Level.SEVERE, null, ex);
        } 
    }//GEN-LAST:event_jButtonCadastrarViagemMouseClicked

